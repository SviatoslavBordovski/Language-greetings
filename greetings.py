def greetings(language): #function that is just remembered by python                                
    if language == 'es':
        print('Hola, muchachos! =)')
    elif language == 'fr':
        print('Bonjour, musie!')
    else:
        print('GO AWAY!')
# end of function that is recorded by python

greetings('fr') # invoke of function depending on chosen 'es'/'fr'/'other' language
